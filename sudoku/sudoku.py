def verification (x, y, n) :
    global g
    
    for c0 in range(len(g)):
        if g[x][c0] == n :
            return False
     
    for l0 in range(len(g)):
        if g[l0][y] == n :
            return False
    l0=(x//3)*3
    c0=(y//3)*3 
    for i in range(0,3) :
        for j in range(0,3) :
            if g[l0+i][c0+j] == n :
                return False                      
    return True   

